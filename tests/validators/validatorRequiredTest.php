namespace Tests\Validators;

use PHPUnit\Framework\TestCase;
use Pnhs\FormValidator\Validator;

class validatorRequiredTest extends TestCase
{
  public function testPushAndPop()
  {
    $form = [
      "input1" => "nicolahsss",
      "input2" => "",
      "input3" => 465,
      "input5" => 4.56,
    ];

    $validator = new Validator($form);

    $input1 = $validator->rules('input1', 'required');
    $input2 = $validator->rules('input2', 'required');
    $input3 = $validator->rules('input3', 'required');
    $input4 = $validator->rules('input4', 'required#26');
    $input5 = $validator->rules('input5', 'required');

    $errors = $validator->errors();

    $this->assertEquals($input1, $form['input1']);
    $this->assertEquals($input2, $form['input2']);
    $this->assertEquals($input3, $form['input3']);
    $this->assertEquals($errors[0]['code'], 26);
    $this->assertEquals($input5, $form['input5']);
  }
}
