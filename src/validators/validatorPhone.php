namespace PNHS\Validator\validators;

use PNHS\Validator\ValidatorInterface;

/**
 * Description of modelMin
 *
 * @author nicolahsss
 */
class validatorPhone implements validatorInterface
{
    private $value;
    private $option;
    private $error = null;
    private $code = null;

    public function setValue($value): void
    {
        $this->value = $value;
    }

    public function setOption(string $option): void
    {
        $this->option = $option;
    }

    public function setCode(int $code): void
    {
        $this->code = $code;
    }

    public function execute()
    {
        if (!empty($this->option) || !empty($this->value))
            if (method_exists($this, $this->option)) {
                return (call_user_func(array($this, $this->option)));
            } else {
                $this->error = "must be a valid country phone";
                return false;
            }
    }

    private function br()
    {
        if ((is_null($this->value)) || (empty($this->value)))
            return $this->value;
        if (!(
            (!is_null($this->value)) &&
            (is_numeric($this->value)) &&
            (strlen($this->value) >= 10) &&
            (strlen($this->value) <= 11))) {
            $this->error = "must be a valid Brazilian phone";
            return false;
        }
        return $this->value;
    }

    public function error()
    {
        return $this->error;
    }

    public function code()
    {
        return $this->code;
    }
}
