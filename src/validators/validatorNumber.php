namespace PNHS\Validator\validators;

use PNHS\Validator\ValidatorInterface;

/**
 * Description of modelMin
 *
 * @author nicolahsss
 */
class validatorNumber implements validatorInterface
{
    private $value;
    private $option;
    private $error = null;
    private $code = null;

    public function setValue($value): void
    {
        $this->value = $value;
    }

    public function setOption(string $option): void
    {
        $this->option = $option;
    }

    public function setCode(int $code): void
    {
        $this->code = $code;
    }

    public function execute()
    {
        // ""
        // Nao = true
        // Nao = False
        // Nao = True
        //# A-Z
        // Nao = true
        // Sim = true
        // Nao = true
        //# 0-9
        // Nao = true
        // Sim = true
        // Sim = false
        if ((!is_null($this->value)) && ($this->value !== "") && (!is_numeric($this->value))) {
            $this->error = "must contain only numbers";
            return false;
        }
        return $this->value;
    }

    public function error()
    {
        return $this->error;
    }

    public function code()
    {
        return $this->code;
    }
}
