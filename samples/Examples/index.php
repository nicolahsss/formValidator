function name($file)
{
    $name = substr($file, 0, -4);
    return ucfirst($name);
}

$dir = dir(__DIR__);
while ($file = $dir->read()) {
    if (!in_array($file, ['.', 'index.php', '..', 'README.md']))
        echo "<a href='Examples/" . $file . "'>" . name($file) . "</a><br />";
}
$dir->close();
